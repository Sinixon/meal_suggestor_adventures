# this class is here to make an instance of the MainCharacter as an object
class User:

    def __init__(self, name, age, sex, activitylevel, weight, height, goal, dailycalories):
        self.name = name
        self.age = age
        self.sex = sex
        self.activitylevel = activitylevel
        self.weight = weight
        self.height = height
        self.goal = goal
        self.dailycalories = dailycalories

# getters
def get_name(self):
    return self._name

def get_sex(self):
    return self._sex

def get_weight(self):
    return self._weight

def get_age(self):
    return self._age

def get_height(self):
    return self._height

def get_activitylevel(self):
    return self._activitylevel

def get_goal(self):
    return self._goal

def get_dailycalories(self):
    return self._dailycalories

# setters
def set_name(self, name):
    self._name = name

def set_age(self, age):
    self._age = age

def set_sex(self, sex):
    self._sex = sex

def set_activitylevel(self, activitylevel):
    self._activitylevel = activitylevel

def set_weight(self, weight):
    self._weight = weight

def set_height(self, height):
    self._height = height

def set_goal(self, goal):
    self._goal = goal

def set_dailycalories(self, dailycalories):
    self._dailycalories = dailycalories